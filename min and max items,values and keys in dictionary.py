 # min and max keys in dct
d = {'pen':5,'pan':10,'eraser':2}
c = max(d.keys())
d = min(d.keys())
print(c,d)
# min and max values in dct 
d = {'pen':5,'pan':10,'eraser':2}
c = max(d.values())
d = min(d.values())
print(c,d)
# min and max items in dct 
d = {'pen':5,'pan':10,'eraser':2}
c = max(d.items())
d = min(d.items())
print(c,d)

